    private void btnKonversiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKonversiActionPerformed
         try {
        double nilai = Double.parseDouble(txtInputSuhu.getText());
        String asal = cmbSkalaAsal.getSelectedItem().toString();
        String tujuan = cmbSkalaTujuan.getSelectedItem().toString();
        double hasil = konversiSuhu(nilai, asal, tujuan);
        lblHasil.setText(String.format("Hasil: %.2f Â°%s", hasil, tujuan.charAt(0)));
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnKonversiActionPerformed

