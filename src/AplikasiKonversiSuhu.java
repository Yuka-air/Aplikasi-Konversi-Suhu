    private double konversiSuhu(double nilai, String asal, String tujuan) {
    double hasil = nilai;

    // Konversi semua ke Celcius dulu
    if (asal.equals("Fahrenheit")) hasil = (nilai - 32) * 5/9;
    else if (asal.equals("Reamur")) hasil = nilai * 5/4;
    else if (asal.equals("Kelvin")) hasil = nilai - 273.15;

    // Dari Celcius ke tujuan
    if (tujuan.equals("Fahrenheit")) hasil = (hasil * 9/5) + 32;
    else if (tujuan.equals("Reamur")) hasil = hasil * 4/5;
    else if (tujuan.equals("Kelvin")) hasil = hasil + 273.15;

    return hasil;
}
    
    
